# Election updater helper functions
def prob_conf_conversion(distr_score):
  calibrated_score = 1/(1+np.exp(-(EFF_LOGIT_A * distr_score + EFF_LOGIT_B)))
  return calibrated_score

def collect_default_group_control(district_id, partition,race):
    district_dict = {}
    district_dict[f'{race}_pop'] = partition[race][district_id]
    district_dict['total_pop'] = partition["population"][district_id]
    return district_dict
def evaluate_default_group_control(district_dict, race):
  if(district_dict['total_pop'] == 0):
    return 0
  group_control = min(1,district_dict[f'{race}_pop']/district_dict['total_pop'] * 2)
  return group_control
def calculate_effectiveness_score(partition,race,group_control_collect_function,
                                  group_control_evaluate_function):
  district_eff_scores= {}


  in_group_score = IN_GROUP_ALL_RACE[race]
  ei_conf = EI_CONF_ALL_RACE[race]
  recency = RECENCY_ELECTION_SCORE
  for district in (range(NUM_DISTRICTS)):
    district = district +1

    district_dict = group_control_collect_function(district,partition,race)
    district_score = recency * ei_conf * in_group_score
    group_control = group_control_evaluate_function(district_dict, race)
    district_score *= group_control
    district_calibrated_score = prob_conf_conversion(district_score)
    district_eff_scores[district] = district_calibrated_score


  return district_eff_scores

def calculate_default_eff_score(partition,race):
  return calculate_effectiveness_score(partition,race,
                 collect_default_group_control,
                 evaluate_default_group_control)

def calculate_effective_district_bool(partition,race):
  return {
        district: partition[f"{race}_eff_score"][district] > EFFECTIVENESS_SCORE_THRESHOLD
        for district in partition.parts
    }


def make_updaters(election=True, minority_sig_groups = None, include_white_pop = None, min_eff =None):
  sig_groups = [*minority_sig_groups, Race.WHITE.value] if include_white_pop else minority_sig_groups
  sig_updater = {f"{minority}":updaters.Tally(f"{minority}", alias=f"{minority}", dtype= int)  for minority in sig_groups}
  my_updaters = {"population": updaters.Tally("total", alias="population")}
  my_updaters.update(sig_updater)

  if(election):
    election_updaters = {election.name: election for election in elections}
    my_updaters.update(election_updaters)

  if(min_eff):
    eff_score = {f'{sig_race}_eff_score': partial(calculate_default_eff_score, race=sig_race) for sig_race in sig_groups}
    eff_bool = {f'{sig_race}_eff_bool': partial(calculate_effective_district_bool, race=sig_race) for sig_race in sig_groups}
    my_updaters.update(eff_score)
    my_updaters.update(eff_bool)
  return my_updaters


