@ray.remote
def parallel_worker(steps, chain_ident,step_tracker, prefix_out_file):
  chain = make_chain_from_data(steps)
  o_file = f"{prefix_out_file}_{chain_ident}.jsonl" # O_file should be unique
  seed_int = convert_string_to_seed(o_file)
  random.seed(seed_int)
  np.random.seed(seed_int)
  run_chain(o_file,ITERATIONS_TO_STORE,chain,CHAIN_STORE_DISTR_PLAN, parallel_step_tracker= step_tracker)
  return chain_ident

def parallel_main(steps, prefix_out_file,number_of_threads):
  tracker = ProgressTracker.remote(steps)
  steps_per_worker = -(steps // -number_of_threads) # Weird ceiling division method in python, shouldn't be an issue on Seawulf
  pending_refs = [parallel_worker.remote(steps_per_worker, i,
                                         tracker,prefix_out_file)
                                         for i in range(number_of_threads)]
  threads_completed= 0
  try:
      while threads_completed < number_of_threads:
          # Wait for any 1 task to finish
          ready_refs, pending_refs = ray.wait(pending_refs, num_returns=1)

          # Get but don't actually store, the return values aren't useful in this case
          # mainly the fact that it finished is useful (apparently synch issues in class method)
          ray.get(ready_refs[0])
          threads_completed += 1
  finally:
      # print(f"Threshold reached. Canceling {len(pending_refs)} remaining tasks.")
      for ref in pending_refs:
          ray.cancel(ref, force=True)
  return range(number_of_threads)
def parallel_run_complete_chain(steps, o_file, number_of_threads):
  for i in range(number_of_threads):
    outp_file = f"{o_file}_{i}.jsonl"
    before_chain(out_file=outp_file)

  parallel_chain_init(number_of_threads)
  chain_identifiers = parallel_main(steps, prefix_out_file= o_file, number_of_threads=number_of_threads)
  after_chain(o_file, chain_identifiers)


