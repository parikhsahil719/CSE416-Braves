def before_chain(out_file):
  # Start the array input
  with open(out_file, "w") as f: # Using with should auto close it safely in the case of an error
    f.write('[')
def after_chain(out_file, chain_ids = None):


  # We have a ',' in the outputs, clean by removing
  def remove_last_char(out_file):
    with open(out_file, 'rb+') as f:
      f.seek(0, os.SEEK_END)
      # Verify not empty
      pos = f.tell()
      if pos > 0:
          # Remove the last comma
          f.seek(pos - 1)
          f.truncate()
  # Make a complete json file
  def add_last_array_char(out_file):
    with open(out_file, "a") as f:
      f.write(']')
  def clean_runs_output(out_file):
    remove_last_char(out_file)
    add_last_array_char(out_file)

  # Remove the last ","
  if (chain_ids == None):
    clean_runs_output(out_file=out_file)
  else:
    for end_suffix in chain_ids:
      o_file = f"{out_file}_{end_suffix}.jsonl"
      clean_runs_output(out_file=o_file)

