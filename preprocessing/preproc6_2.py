# Census race extraction w/ geometry
def get_census_race_data(pl_zip_name):


    # Load the zipped files

    with zipfile.ZipFile(pl_zip_name, 'r') as z:
        # locate files by how its named
        geo_fname   = next(f for f in z.namelist() if "geo2020.pl"     in f.lower())
        seg1_fname = next(f for f in z.namelist() if "000022020.pl" in f.lower()) # VAP data



        with z.open(geo_fname) as fh:
            geo = (pd.read_csv(fh, sep='|', header=None, dtype=str, encoding='latin-1')
                     .apply(lambda s: s.str.strip()))

        with z.open(seg1_fname) as fh:
            race = (pd.read_csv(fh, sep='|', header=None, dtype=str, encoding='latin-1')
                      .apply(lambda s: s.str.strip()))

    blocks = geo[geo[2] == '750'].copy()


    # Per PL spec: LOGRECNO is at geo col 7 and race col 4.
    GEO_LOGRECNO_COL  = 7
    RACE_LOGRECNO_COL = 4


    # Quick sanity-check overlap before continuing
    # geo_set  = set(blocks[GEO_LOGRECNO_COL].astype(str).str.lstrip('0').unique())
    # race_set = set(race[RACE_LOGRECNO_COL].astype(str).str.lstrip('0').unique())
    # overlap  = len(geo_set & race_set)
    # print(f"Bridge verified: {overlap:,} / {len(blocks):,} block LOGRECNOs found in race file.")
    # if overlap < len(blocks) * 0.95:
    #     raise RuntimeError("ERROR ON OVERLAPPING")

    GEOID_COL = 8

    # Remove extra details that make the data not overlap well
    final_geo = pd.DataFrame({
        'LOGRECNO': blocks[GEO_LOGRECNO_COL].astype(str).str.strip().str.lstrip('0'),
        'GEOID':    blocks[GEOID_COL].str.split('US').str[-1]
    })
    print(f" [Census.3] GEOID sample: {final_geo['GEOID'].head().tolist()}")


    # Race extraction from the file
    final_race = pd.DataFrame({
        'LOGRECNO': race[RACE_LOGRECNO_COL].astype(str).str.strip().str.lstrip('0'),
        'total':    pd.to_numeric(race[5],  errors='coerce'),
        'white':    pd.to_numeric(race[80],  errors='coerce'),
        'black':    pd.to_numeric(race[81],  errors='coerce'),
        'asian':    pd.to_numeric(race[83], errors='coerce'),   # FIXED: was race[9]
        'hispanic': pd.to_numeric(race[77], errors='coerce'),    # FIXED: was race[11]
        # Other = total - white - black - hispanic - asian(asian was kept as they seemed to be high in oregon, may not be eligible however)
    }).fillna(0)




    result = final_geo.merge(final_race, on='LOGRECNO', how='inner')

    total_pop = result['total'].sum()
    print(f" {len(result):,} blocks | total pop = {total_pop}")
    print(f" Blocks with pop > 0: {(result['total'] > 0).sum():,}")
    # research shows zeros may be empty areas like water or large parks so not unreasonable

    return result


# State level

def process_state(state_name, uploaded):

    short = "or" if "oregon" in state_name.lower() else "sc"
    fips  = "41" if short == "or" else "45"
    lower = state_name.lower().replace(" ", "_")

    prec_file = next(f for f in uploaded
                     if short in f.lower() and "precincts" in f.lower()
                     and f.endswith('.geojson'))
    precincts = gpd.read_file(uploaded[prec_file]).to_crs("EPSG:3857")
    precincts.geometry = precincts.geometry.buffer(0)
    if 'precinct_id' not in precincts.columns:
        precincts['precinct_id'] = precincts.index.astype(str)
    print(f" Precincts loaded: {len(precincts)} precincts.")



    tiger_url = (f"https://www2.census.gov/geo/tiger/TIGER2020/TABBLOCK20/"
                 f"tl_2020_{fips}_tabblock20.zip")
    # Voting data
    blocks = gpd.read_file(tiger_url).to_crs(precincts.crs)
    blocks.geometry = blocks.geometry.buffer(0)

    pl_zip = next(f for f in uploaded
                  if short in f.lower() and "2020.pl.zip" in f.lower())
    race_table = get_census_race_data(uploaded[pl_zip])

    geoid_col = 'GEOID20' if 'GEOID20' in blocks.columns else 'GEOID'
    blocks[geoid_col] = blocks[geoid_col].astype(str).str.zfill(15)
    # race_table['GEOID'] is already a clean 15-digit string from get_census_race_data
    blocks = blocks.merge(race_table[['GEOID','total','white','black','asian','hispanic']],
                          left_on=geoid_col, right_on='GEOID', how='inner')

    assignment = maup.assign(blocks.geometry, precincts.geometry)
    # print(precincts.keys())

    race_cols  = ['total', 'white', 'black', 'asian', 'hispanic']
    
    precincts[race_cols] = (blocks[race_cols]
                            .groupby(assignment)
                            .sum()
                            .reindex(precincts.index)
                            .fillna(0)
                            .astype(int))
    precincts['other'] = (precincts['total']
                     - precincts['white']
                     - precincts['black']
                     - precincts['asian']
                     - precincts['hispanic']).clip(lower=0)
    race_cols.append('other')


    # Precinct -> Districts
    dist_file = next(f for f in uploaded
                     if lower in f.lower() and 'district' in f.lower()
                     and f.endswith('.geojson'))
    districts = gpd.read_file(uploaded[dist_file]).to_crs(precincts.crs)
    districts.geometry = districts.geometry.buffer(0)

    # Use precinct centroid for assignment for safety
    prec_centroids = precincts.copy()
    prec_centroids.geometry = prec_centroids.geometry.centroid
    dist_join = gpd.sjoin(prec_centroids, districts, how='left', predicate='within')
    

    dist_col = 'CD118FP' if 'CD118FP' in districts.columns else 'district'
    precincts['district_id'] = dist_join[dist_col].fillna("0").values


    state_abbr = 'OR' if 'oregon' in state_name.lower() else 'SC'
    results_file = next(f for f in uploaded
                        if state_abbr in f.upper() and 'precincts-with-results' in f
                        and f.endswith('.csv'))

    results_df = pd.read_csv(uploaded[results_file])

    # The GeoJSON precinct_id should match results_df GEOID.
    # If the anchor GeoJSON doesn't carry GEOID, align by position or add a
    # county-precinct key that matches the CSV format.
    if 'GEOID' in precincts.columns:
        election_df = (precincts[['precinct_id', 'GEOID']]
                       .merge(results_df[['GEOID','votes_dem','votes_rep','votes_total']],
                              on='GEOID', how='left'))
    else:
        # Fallback: CSV and GeoJSON rows may be in the same precinct order...
        print(" No 'GEOID' column found in precinct GeoJSON. "
              "Aligning election CSV by row order (verify this is correct).")
        election_df = pd.DataFrame({
            'precinct_id':       precincts['precinct_id'].values,
            'democratic_votes':  results_df['votes_dem'].values,
            'republican_votes':  results_df['votes_rep'].values,
            'total_votes':       results_df['votes_total'].values
        })

    election_df = election_df.rename(columns={
        'votes_dem': 'democratic_votes',
        'votes_rep': 'republican_votes',
        'votes_total': 'total_votes'
    }).fillna(0)
    election_df[['democratic_votes','republican_votes','total_votes']] = (
        election_df[['democratic_votes','republican_votes','total_votes']].astype(int)
    )
    print(f"  ✅ Election data loaded. "
          f"Total dem votes: {election_df['democratic_votes'].sum():,.0f}  "
          f"Total rep votes: {election_df['republican_votes'].sum():,.0f}")


    if "oregon" in state_name.lower():
        reps = {
            1: {"name": "Suzanne Bonamici", "party": "Democrat",    "race": "White",    "margin": 0.58},
            2: {"name": "Cliff Bentz",       "party": "Republican",  "race": "White",    "margin": 0.62},
            3: {"name": "Maxine Dexter",     "party": "Democrat",    "race": "White",    "margin": 0.71},
            4: {"name": "Val Hoyle",         "party": "Democrat",    "race": "White",    "margin": 0.52},
            5: {"name": "Janelle Bynum",     "party": "Democrat",    "race": "Black",    "margin": 0.51},
            6: {"name": "Andrea Salinas",    "party": "Democrat",    "race": "Hispanic", "margin": 0.55},
        }
    else:  # SC
        reps = {
            1: {"name": "Nancy Mace",        "party": "Republican",  "race": "White",    "margin": 0.55},
            2: {"name": "Joe Wilson",        "party": "Republican",  "race": "White",    "margin": 0.60},
            3: {"name": "Jeff Duncan",       "party": "Republican",  "race": "White",    "margin": 0.68},
            4: {"name": "William Timmons",   "party": "Republican",  "race": "White",    "margin": 0.62},
            5: {"name": "Ralph Norman",      "party": "Republican",  "race": "White",    "margin": 0.59},
            6: {"name": "Jim Clyburn",       "party": "Democrat",    "race": "Black",    "margin": 0.65},
            7: {"name": "Russell Fry",       "party": "Republican",  "race": "White",    "margin": 0.58},
        }

    incumbent_rows = []
    for _, row in precincts.iterrows():
        try:
            dist_num = int(row['district_id'])
        except (ValueError, TypeError):
            dist_num = 0
        rep = reps.get(dist_num, {"name": "Unknown", "party": "Unknown",
                                  "race": "Unknown", "margin": 0.0})
        incumbent_rows.append({
            'precinct_id':      row['precinct_id'],
            'district':         dist_num,
            'representative':   rep['name'],
            'party':            rep['party'],
            'incumbent_race':   rep['race'],
            'margin':           rep['margin']
        })
    incumbent_df = pd.DataFrame(incumbent_rows)

    print("\n📍 [7/7] Writing output files...")
    VAP_DIR = '/content/drive/MyDrive/U4/Spring/CSE416/Preprocessing/VAP2/my_preprocessing1_out/'
    output_dir = f'{VAP_DIR}/{lower}'
    os.makedirs(output_dir, exist_ok=True)


    precincts[['precinct_id', 'district_id', 'geometry']].to_file(
        f'{output_dir}/precincts.geojson', driver='GeoJSON')


    demo_df = precincts[['precinct_id'] + race_cols].copy()
    demo_df.to_csv(f'{output_dir}/demographics.csv', index=False)


    election_df.to_csv(f'{output_dir}/election_2024.csv', index=False)


    incumbent_df.to_csv(f'{output_dir}/incumbents.csv', index=False)


    combined = (demo_df
                .merge(election_df.drop(columns=['GEOID'], errors='ignore'),
                       on='precinct_id', how='left')
                .merge(incumbent_df,
                       on='precinct_id', how='left'))
    combined.to_csv(f'{output_dir}/combined.csv', index=False)




