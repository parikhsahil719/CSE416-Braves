GAP_TOLERANCE_FT = 200.0   # maximum gap between two boundaries to still be neighbors
MIN_SHARED_FT    = 200.0   # minimum parallel boundary run to count as rook adjacency

# Convert to metres (UTM CRS)
FT_TO_M        = 0.3048
GAP_TOLERANCE_M = GAP_TOLERANCE_FT * FT_TO_M   # 60.96 m
MIN_SHARED_M    = MIN_SHARED_FT    * FT_TO_M   # 60.96 m

def fuzzy_rook(g1, g2, gap_m: float, min_shared_m: float) -> (bool, float):

    def _longest_run(geom) -> float:
        if geom.is_empty:
            return 0.0
        if geom.geom_type == 'LineString':
            return geom.length
        if geom.geom_type == 'MultiLineString':
            return max(g.length for g in geom.geoms)

        runs = [g.length for g in geom.geoms
                if g.geom_type in ('LineString', 'MultiLineString')]
        return max(runs) if runs else 0.0


    sp = shapely.shared_paths(g1.boundary, g2.boundary)
    shared = sum(geom.length for geom in sp.geoms)
    if shared >= min_shared_m:
        return (True, shared)          

    seg1 = g1.boundary.intersection(g2.buffer(gap_m))   
    seg2 = g2.boundary.intersection(g1.buffer(gap_m))   

    run1 = _longest_run(seg1)
    run2 = _longest_run(seg2)
    min_run = min(run1, run2)
    if min_run >= min_shared_m:
        return (True, min_run)
    return (False, min_run)

def build_fuzzy_graph(state_dir: str, state_label: str):

    graph = gerrychain.Graph.from_json(f'{state_dir}/graphs/precinct_graph.json')
    gdf   = gpd.read_file(f'{state_dir}/data/precincts.geojson').set_index('precinct_id')

    NODE_ID_TYPE = str
    gdf.index    = gdf.index.map(NODE_ID_TYPE)
    print(f"  Index normalised to str. Sample node: {repr(next(iter(graph.nodes())))}")


    if gdf.crs is None or gdf.crs.is_geographic:
        raise ValueError(
            "CRS must be a projected (metre-unit) system. "
        )

    empty_mask = gdf.geometry.is_empty | gdf.geometry.isna()
    n_empty    = empty_mask.sum()
    if n_empty:
        print(f"  ⚠️  {n_empty} empty geometries — run island patch first: "
              f"{gdf.index[empty_mask].tolist()}")

    valid_gdf = gdf[~empty_mask].copy()
    geoms     = list(valid_gdf.geometry)
    ids       = [NODE_ID_TYPE(x) for x in valid_gdf.index]

    sindex = STRtree(geoms)

    import time
    t0 = time.time()

    new_edges       = []
    skipped_corners = 0
    already_present = 0
    pairs_checked   = 0

    for i, (pid, geom) in enumerate(zip(ids, geoms)):

        candidates = sindex.query(geom.buffer(GAP_TOLERANCE_M))

        for j in candidates:
            if j <= i:
                continue   # avoid double-counting; (i,j) only processed once

            other_pid  = ids[j]
            other_geom = geoms[j]
            pairs_checked += 1

            # Skip if edge already in graph
            if graph.has_edge(pid, other_pid):
                already_present += 1
                continue

            # Explicit two-condition AND (spec: >= 200ft shared AND within 200ft)
            is_neighbor, edge_length = fuzzy_rook(geom, other_geom, GAP_TOLERANCE_M, MIN_SHARED_M)

            if is_neighbor:
                new_edges.append((pid, other_pid,edge_length))
            else:
                skipped_corners += 1

    elapsed = time.time() - t0

    for u, v, shared_len in new_edges:
        graph.add_edge(u, v, shared_perim=shared_len, fuzzy_rook=True)

    if new_edges:
        sample = new_edges[:5]
        print(f"  Sample new edges: {[(u,v) for u,v,_ in sample]}")

    remaining_islands = set(graph.islands)

    if not nx.is_connected(graph):
        n_comp = nx.number_connected_components(graph)
        print(f"  ⚠️  {n_comp} components — disconnection not caused by geometry.")


    graph.to_json(f'{state_dir}/graphs/precinct_graph.json',
                  include_geometries_as_geojson=False)


    graph.to_json(f'{state_dir}/graphs/precinct_graph_with_geo.json',
                  include_geometries_as_geojson=True)


    with open(f'{state_dir}/graphs/adjacency_list.txt', 'w') as f:
        for node in sorted(graph.nodes()):
            neighbors = sorted(graph.neighbors(node))
            f.write(f"{node}: {','.join(map(str, neighbors))}\n")


    # Edge audit CSV — useful for reviewing which edges were added by fuzzy pass
    if new_edges:
        audit = pd.DataFrame(new_edges, columns=['precinct_a', 'precinct_b', 'length'])
        audit.to_csv(f'{state_dir}/graphs/fuzzy_rook_new_edges.csv', index=False)


    # Update metadata
    meta_path = f'{state_dir}/metadata.json'
    if os.path.exists(meta_path):
        with open(meta_path) as fh:
            meta = json.load(fh)
        meta['graph_edges']         = graph.number_of_edges()
        meta['graph_connected']     = nx.is_connected(graph)
        meta['islands']             = list(remaining_islands)
        meta['fuzzy_rook_applied']  = True
        meta['fuzzy_rook_gap_ft']   = GAP_TOLERANCE_FT
        meta['fuzzy_rook_min_ft']   = MIN_SHARED_FT
        meta['fuzzy_rook_new_edges']= len(new_edges)
        with open(meta_path, 'w') as fh:
            json.dump(meta, fh, indent=2)
        print(f"  ✓ metadata.json")

    print(f"\n✅ {state_label} done.  "
          f"Edges: {graph.number_of_edges():,}  "
          f"(+{len(new_edges)} fuzzy)  "
          f"Connected: {nx.is_connected(graph)}")



