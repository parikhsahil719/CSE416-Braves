def island_neighbor_length(g1, g2, min_shared_m):

    # Case 1: exact shared edge
    sp = shapely.shared_paths(g1.boundary, g2.boundary)
    shared = sum(geom.length for geom in sp.geoms)
    if shared >= min_shared_m:
        return shared

    # Case 2: absorbed overlap
    g1_in_g2 = g1.boundary.intersection(g2)
    g2_in_g1 = g2.boundary.intersection(g1)
    overlap_contact = max(g1_in_g2.length, g2_in_g1.length)
    if overlap_contact >= min_shared_m:
        return overlap_contact

    # Case 3: micro-gap from coordinate imprecision between source files
    zone = (g1.boundary.buffer(2.5).intersection(g2.boundary.buffer(2.5)))
    if not zone.is_empty:
        micro = min(g1.boundary.intersection(zone).length,
                    g2.boundary.intersection(zone).length)
        if micro >= min_shared_m:
            return micro

    return max(shared, overlap_contact)



