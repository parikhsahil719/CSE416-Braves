# High-Level Flow:
#   For each state (Oregon, South Carolina):
#   1. LOAD      – Read all Prepro-1 outputs (precincts GeoJSON + CSVs).
#   2. MERGE     – Join demographics, elections, and incumbents onto the
#                  GeoDataFrame so every precinct row has all attributes.
#   3. REPAIR    – Apply maup.smart_repair() to fix topology before any
#                  graph work (slivers, gaps, invalid geometries, etc.).
#   4. GRAPH     – Build the adjacency graph via gerrychain.Graph.from_geodataframe()
#                  with rook contiguity — the canonical MGGG method.
#                  All GeoDataFrame columns become node attributes automatically.
#   5. SERIALIZE – Two graph files:
#                    • precinct_graph.json          (no geometry – fast, for SeaWulf runs)
#                    • precinct_graph_with_geo.json (geometry as GeoJSON – for viz)
#                  The companion precincts.geojson from Prepro-1 can be joined to
#                  any post-run output by matching precinct_id, so the viz GeoJSON
#                  doubles as the authoritative geometry reference.
#   6. ADJACENCY – Human-readable adjacency list text file.
#   7. DATA CSVs – Copy CSVs for reference / debugging.
#
# Key Bugs Fixed from Original PREPRO-6:
#   • Graph construction: replaced manual NX + .touches() loop with
#     gerrychain.Graph.from_geodataframe(adjacency='rook').  The manual
#     loop using .touches() misses a large fraction of shared-edge pairs
#     due to floating-point topology irregularities.
#   • Node attributes: the old graph only stored centroid_x/y.  GerryChain
#     needs all race + election + district columns as node attributes.
#     from_geodataframe() copies all GeoDataFrame columns to nodes via
#     cols_to_add automatically.
#   • Serialization: replaced pickle (Python-version-dependent, not portable
#     across machines) with gerrychain's own graph.to_json() which uses the
#     NetworkX adjacency-data JSON format that from_json() can reload exactly.
#   • Two output versions: lightweight (no geometry, ~10× smaller) for the
#     compute nodes, and a geometry-embedded version for visualization pipelines.
#
# GeoJSON / Geometry separation note:
#   GerryChain does NOT need geometry during a MCMC run.  The geometry lives
#   in precincts.geojson.  After a run, join district assignments back to the
#   GeoJSON by precinct_id for choropleth rendering.
#
# ============================================

