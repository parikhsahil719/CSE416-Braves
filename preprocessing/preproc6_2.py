PREPRO1_DIR = f'/content/drive/MyDrive/U4/Spring/CSE416/Preprocessing/VAP2/my_preprocessing1_out'


def process_state(state_name: str):

    lower     = state_name.lower().replace(" ", "_")
    state_dir = f'/content/drive/MyDrive/U4/Spring/CSE416/Preprocessing/VAP2/my_preprocessing1_out/sw_{lower}'
    base      = f'{PREPRO1_DIR}/{lower}'


    precinct_gdf = gpd.read_file(f'{base}/precincts.geojson')
    election_df  = pd.read_csv(f'{base}/election_2024.csv')
    demo_df      = pd.read_csv(f'{base}/demographics.csv')
    incumbent_df = pd.read_csv(f'{base}/incumbents.csv')


    print("\n📍 [2/7] Merging attributes onto GeoDataFrame...")

    precinct_gdf.loc[:, 'precinct_id'] = precinct_gdf['precinct_id'].astype(str).values
    demo_df.loc[:,      'precinct_id'] = demo_df['precinct_id'].astype(str).values
    election_df.loc[:,  'precinct_id'] = election_df['precinct_id'].astype(str).values
    incumbent_df.loc[:, 'precinct_id'] = incumbent_df['precinct_id'].astype(str).values

    # Log dtypes so future mismatches are immediately visible
    print(f"  precinct_id dtypes: "
          f"gdf={precinct_gdf['precinct_id'].dtype}  "
          f"demo={demo_df['precinct_id'].dtype}  "
          f"election={election_df['precinct_id'].dtype}  "
          f"incumbent={incumbent_df['precinct_id'].dtype}")

    gdf_cols       = set(precinct_gdf.columns) - {'precinct_id', 'geometry'}
    demo_cols      = [c for c in demo_df.columns      if c not in gdf_cols | {'precinct_id'}]
    election_cols  = [c for c in election_df.columns  if c not in gdf_cols | {'precinct_id'}]
    incumbent_cols = [c for c in incumbent_df.columns if c not in gdf_cols | {'precinct_id'}]

    gdf = (precinct_gdf
           .merge(demo_df[     ['precinct_id'] + demo_cols],      on='precinct_id', how='left')
           .merge(election_df[ ['precinct_id'] + election_cols],  on='precinct_id', how='left')
           .merge(incumbent_df[['precinct_id'] + incumbent_cols], on='precinct_id', how='left'))


    gdf['district_id'] = pd.to_numeric(gdf['district_id'], errors='coerce').fillna(0).astype(int)


    print(f"  ✓ Combined GDF: {len(gdf):,} rows × {gdf.shape[1]} columns")
    print(f"  ✓ Columns: {gdf.columns.tolist()}")



    if lower == 'oregon':
        utm_crs = 'EPSG:32610'   
    else:
        utm_crs = 'EPSG:32617'   
    gdf = gdf.to_crs(utm_crs)


    print("start maup.smart_repair")
    # Ensure RangeIndex going in — defensive reset in case any earlier step
    # (e.g. a merge) left a non-contiguous index.
    gdf = gdf.reset_index(drop=True)

    gdf_repaired = maup.smart_repair(
        gdf,
        snapped=True,
        snap_precision=8,
        fill_gaps=True,
        fill_gaps_threshold=0.05,

    )
    if len(gdf_repaired) != len(gdf):
        print(f"  ⚠️  Row count changed by repair: {len(gdf)} → {len(gdf_repaired)} "
              f"({len(gdf) - len(gdf_repaired)} fragments removed)")

    # Now safe to promote precinct_id to index for GerryChain node labelling.
    gdf_repaired = gdf_repaired.set_index('precinct_id')
    print(f"  ✓ Repair complete: {len(gdf_repaired):,} precincts retained")


    graph = gerrychain.Graph.from_geodataframe(
        gdf_repaired,
        adjacency='rook',
        cols_to_add=None,      # add ALL columns as node attributes
        reproject=False,       # already in UTM
        ignore_errors=False    # fail loudly on remaining invalid geometries
    )

    print(f" Nodes:     {graph.number_of_nodes():,}")
    print(f" Edges:     {graph.number_of_edges():,}")

    # Island check – degree-0 nodes break GerryChain runs
    islands = graph.islands
    if islands:
        print(f"  ⚠️  Islands (degree-0 nodes): {len(islands)} – "
              f"these will cause GerryChain to fail. IDs: {list(islands)[:10]}...")
        print("     Consider a second maup.smart_repair() pass or manual review.")
    else:
        print("  ✓ No islands detected.")

    is_connected = nx.is_connected(graph)
    print(f"  ✓ Connected: {is_connected}")
    if not is_connected:
        n_comp = nx.number_connected_components(graph)
        print(f"  ⚠️  {n_comp} connected components. "
              f"GerryChain requires a connected graph. "
              f"Flag for future repair pass.")

    graph_path_light = f'{state_dir}/graphs/precinct_graph.json'
    graph_path_geo   = f'{state_dir}/graphs/precinct_graph_with_geo.json'

    graph.to_json(graph_path_light, include_geometries_as_geojson=False)
    print(f"  ✓ Lightweight graph: {graph_path_light}  "
          f"({os.path.getsize(graph_path_light):,} bytes)")

    graph.to_json(graph_path_geo, include_geometries_as_geojson=True)
    print(f"  ✓ Geo-embedded graph: {graph_path_geo}  "
          f"({os.path.getsize(graph_path_geo):,} bytes)")

    # Human-readable adjacency list (sorted for reproducibility)
    adj_path = f'{state_dir}/graphs/adjacency_list.txt'
    with open(adj_path, 'w') as f:
        for node in sorted(graph.nodes()):
            neighbors = sorted(graph.neighbors(node))
            f.write(f"{node}: {','.join(map(str, neighbors))}\n")
    print(f"  ✓ Adjacency list: {adj_path}")



    # Full GeoDataFrame (with all attributes + geometry)
    gdf_repaired.reset_index().to_file(f'{state_dir}/data/precincts.geojson',
                                        driver='GeoJSON')

    # Flat CSVs (no geometry)
    flat_df = gdf_repaired.reset_index().drop(columns=['geometry'])
    flat_df.to_csv(f'{state_dir}/data/combined_data.csv', index=False)

    # Individual source CSVs (for reference)
    election_df.to_csv( f'{state_dir}/data/election_2024.csv', index=False)
    demo_df.to_csv(     f'{state_dir}/data/demographics.csv',  index=False)
    incumbent_df.to_csv(f'{state_dir}/data/incumbents.csv',    index=False)

    # Metadata JSON
    metadata = {
        'state':             state_name,
        'num_precincts':     len(gdf_repaired),
        'num_districts':     int(gdf_repaired['district_id'].nunique()),
        'graph_nodes':       graph.number_of_nodes(),
        'graph_edges':       graph.number_of_edges(),
        'graph_connected':   is_connected,
        'islands':           list(islands),
        'node_attributes':   [k for k in dict(list(graph.nodes(data=True))[0][1]).keys()],
        'crs':               str(gdf_repaired.crs),
        'generated_date':    pd.Timestamp.now().strftime('%Y-%m-%d %H:%M:%S'),
    }
    with open(f'{state_dir}/metadata.json', 'w') as fh:
        json.dump(metadata, fh, indent=2)

    print(f"  ✓ All files written to '{state_dir}/'")
    print(f"\n✅ {state_name} complete.  Node attributes: {metadata['node_attributes']}")

